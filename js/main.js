// ---- Carousel engine ----
const carousels = {};

function cap(s) { return s.charAt(0).toUpperCase() + s.slice(1); }

function buildCarousel(id) {
  const folder = 'img/' + id + '/';
  const images = GALLERY[id];
  const track = document.getElementById('track' + cap(id));
  const dotsEl = document.getElementById('dots' + cap(id));

  images.forEach((file, i) => {
    const slide = document.createElement('div');
    slide.className = 'carousel-slide';
    const img = document.createElement('img');
    img.src = folder + file;
    img.alt = '';
    img.loading = i === 0 ? 'eager' : 'lazy';
    slide.appendChild(img);
    const caption = CAPTIONS[id] && CAPTIONS[id][i];
    if (caption) {
      const cap_el = document.createElement('div');
      cap_el.className = 'carousel-caption';
      cap_el.textContent = caption;
      slide.appendChild(cap_el);
    }
    track.appendChild(slide);

    const dot = document.createElement('button');
    dot.className = 'carousel-dot' + (i === 0 ? ' active' : '');
    dot.setAttribute('aria-label', 'Слайд ' + (i + 1));
    dot.onclick = () => goToSlide(id, i);
    dotsEl.appendChild(dot);
  });

  carousels[id] = { index: 0, count: images.length };
}

function goToSlide(id, idx) {
  const c = carousels[id];
  c.index = (idx + c.count) % c.count;
  document.getElementById('track' + cap(id)).style.transform = `translateX(-${c.index * 100}%)`;
  document.querySelectorAll(`#dots${cap(id)} .carousel-dot`).forEach((d, i) => {
    d.classList.toggle('active', i === c.index);
  });
}

function moveCarousel(id, dir) { goToSlide(id, carousels[id].index + dir); }

function addSwipe(trackId, id) {
  const el = document.getElementById(trackId);
  let startX = 0;
  el.addEventListener('touchstart', e => { startX = e.touches[0].clientX; }, { passive: true });
  el.addEventListener('touchend', e => {
    const dx = e.changedTouches[0].clientX - startX;
    if (Math.abs(dx) > 40) moveCarousel(id, dx < 0 ? 1 : -1);
  }, { passive: true });
}

// ---- Init on load ----
document.addEventListener('DOMContentLoaded', () => {
  ['wagons', 'base', 'accommodation'].forEach(id => {
    buildCarousel(id);
    addSwipe('track' + cap(id), id);
    setInterval(() => moveCarousel(id, 1), 5000);
  });

  // Cookie banner
  const banner = document.getElementById('cookieBanner');
  const acceptBtn = document.getElementById('cookieAccept');
  if (banner && acceptBtn) {
    if (!localStorage.getItem('cookie_consent')) {
      banner.style.display = 'block';
    }
    acceptBtn.addEventListener('click', function () {
      localStorage.setItem('cookie_consent', '1');
      banner.style.display = 'none';
    });
  }

  // Mobile menu
  const burger = document.getElementById('burger');
  const mobileMenu = document.getElementById('mobileMenu');
  burger.addEventListener('click', () => mobileMenu.classList.toggle('open'));
  mobileMenu.querySelectorAll('a').forEach(a => a.addEventListener('click', () => mobileMenu.classList.remove('open')));

  // Contact form
  document.getElementById('contactForm').addEventListener('submit', function(e) {
    e.preventDefault();
    this.style.display = 'none';
    document.getElementById('formSuccess').style.display = 'block';
  });

  // Mobile phone: expand on tap
  const navPhone = document.querySelector('.nav-phone');
  if (navPhone) {
    navPhone.addEventListener('click', function(e) {
      if (window.innerWidth <= 640 && !this.classList.contains('expanded')) {
        e.preventDefault();
        this.classList.add('expanded');
      }
    });
    document.addEventListener('click', e => {
      if (!navPhone.contains(e.target)) navPhone.classList.remove('expanded');
    });
  }

  // Nav highlight on scroll
  const sections = document.querySelectorAll('section[id]');
  const navLinks = document.querySelectorAll('.nav-links a[href^="#"]');
  new IntersectionObserver(entries => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        navLinks.forEach(l => l.classList.remove('active'));
        const active = document.querySelector(`.nav-links a[href="#${entry.target.id}"]`);
        if (active) active.classList.add('active');
      }
    });
  }, { threshold: 0.4 }).observe && sections.forEach(s =>
    new IntersectionObserver(entries => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          navLinks.forEach(l => l.classList.remove('active'));
          const a = document.querySelector(`.nav-links a[href="#${entry.target.id}"]`);
          if (a) a.classList.add('active');
        }
      });
    }, { threshold: 0.4 }).observe(s)
  );
});
